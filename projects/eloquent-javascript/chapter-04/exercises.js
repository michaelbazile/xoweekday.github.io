// range ///////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function range(start, end, step) {
let result = [];
if(step !== undefined && step > 0){
  for(let i = start; i <= end; i += step){
    result.push(i);
  }
} else if(Math.sign(step) === -1){
  for(let i = start; i >= end; i -= step){
    result.push(i);
  }
} else if(start === end)  {
  return result;
} else {
  for(let i = start; i <= end; i++){
    result.push(i);
  }
}
return result;
}


////////////////////////////////////////////////////////////////////////////////
// sum /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function sum(array) {
let count = 0;

array.forEach((i)=>{
  count += i;
});

return count;
}

////////////////////////////////////////////////////////////////////////////////
// reverseArray ////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function reverseArray(array) {
  let arr = [];
for(let i = array.length - 1; i >= 0; i--){
  arr.push(array[i]);
}
return arr;
}

////////////////////////////////////////////////////////////////////////////////
// reverseArrayInPlace /////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function reverseArrayInPlace(array) {
let arr = [];
for(let i = Math.floor(array.length / 2);  i >= 0; i--){
  arr.push(array[array.length - 1 - i]);
}
return arr;
}

